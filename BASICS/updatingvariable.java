package BASICS;

public class updatingvariable {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x = 7;
        System.out.println(x);
        x = x + 5;
        System.out.println(x);
        x = x - 17;
        System.out.println(x);
    
    }
    
}
